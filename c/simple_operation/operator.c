#include <stdio.h>

int main() {

    int x = 5;
    int y = 9;

    printf("Hasil X < Y: %d \n", x < y);
    printf("Hasil X > Y: %d \n", x > y);
    printf("Hasil X <= Y: %d \n", x <= y);
    printf("Hasil X =< Y: %d \n", x >= y);
    printf("Hasil X == Y: %d \n", x == y);
    printf("Hasil X != Y: %d \n", x != y);

    return 0;

}